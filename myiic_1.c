//产生IIC起始信号
void IIC_1_Start(void)
{
	SDA_1_OUT();     //sda线输出
	IIC_SDA_1=1;	  	  
	IIC_SCL_1=1;
	delay_us(4);
 	IIC_SDA_1=0;//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	IIC_SCL_1=0;//钳住I2C总线，准备发送或接收数据 
}	  
//产生IIC停止信号
void IIC_1_Stop(void)
{
	SDA_1_OUT();//sda线输出
	IIC_SCL_1=0;
	IIC_SDA_1=0;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	IIC_SCL_1=1; 
	IIC_SDA_1=1;//发送I2C总线结束信号
	delay_us(4);							   	
}
//等待应答信号到来
//返回值：0，接收应答失败
//        1，接收应答成功
u8 IIC_1_Wait_Ack(void)
{
	u8 ucErrTime=0;
	SDA_1_IN();      //SDA设置为输入  
	IIC_SDA_1=1;delay_us(1);	   
	IIC_SCL_1=1;delay_us(1);	 
	while(READ_SDA_1)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			IIC_1_Stop();
			return 0;
		}
	}
	IIC_SCL_1=0;//时钟输出0 	   
	return 1;  
} 
//产生ACK应答
void IIC_1_Ack(void)
{
	IIC_SCL_1=0;
	SDA_1_OUT();
	IIC_SDA_1=0;
	delay_us(2);
	IIC_SCL_1=1;
	delay_us(2);
	IIC_SCL_1=0;
}
//不产生ACK应答		    
void IIC_1_NAck(void)
{
	IIC_SCL_1=0;
	SDA_1_OUT();
	IIC_SDA_1=1;
	delay_us(2);
	IIC_SCL_1=1;
	delay_us(2);
	IIC_SCL_1=0;
}					 				     
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void IIC_1_Send_Byte(u8 txd)
{                        
    u8 t;   
	SDA_1_OUT(); 	    
    IIC_SCL_1=0;//拉低时钟开始数据传输
    for(t=0;t<8;t++)
    {              
        //IIC_SDA=(txd&0x80)>>7;
		if((txd&0x80)>>7)
			IIC_SDA_1=1;
		else
			IIC_SDA_1=0;
		txd<<=1; 	  
		delay_us(2);   //对TEA5767这三个延时都是必须的
		IIC_SCL_1=1;
		delay_us(2); 
		IIC_SCL_1=0;	
		delay_us(2);
    }	 
} 	    
